package services;

/**
 * Used to define the size of a thumbnail.
 */
public enum ThumbnailSize {
	SMALL(130, 130), BIG(1100, 1100);
	
	private final int width;
	private final int height;
	
	private ThumbnailSize(int width, int height){
		this.width = width;
		this.height = height;
	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}
}
